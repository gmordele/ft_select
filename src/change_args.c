void	change_args(int argc, char **argv)
{
	int	i;
	int j;

	i = 1;
	while (i < argc)
	{
		j = 0;
		while (argv[i][j] != '\0')
		{
			if (argv[i][j] < 32 || argv[i][j] > 126)
				argv[i][j] = ' ';
			++j;
		}
		++i;
	}
}
